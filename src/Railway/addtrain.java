    public void Train_Details()//To show in the table in the gui
    {
        try {
            pst = con.prepareStatement("select * from traindetails");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int c;//getting the coloumn number in the interger c, fetched from the database table
            c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();//casting done by using (DefaultTableModel).
            //creating runtime table (by getting the column number and row number.
            d.setRowCount(0);
            while (rs.next())///rs.next() As long as the database table has rows of information in it
            {
               Vector v = new Vector ();///creating a vector type object, 
               for (int i = 0; i <= c; i++)
               {
                   v.add (rs.getString("trainno"));
                   v.add (rs.getString("trainname"));
                   v.add (rs.getString("startplace"));
                   v.add (rs.getString("destplace"));
                   v.add (rs.getString("price"));
               }
            d.addRow(v);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(addtrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ctno = txtno.getText();
        String ctrainname = tname.getText();
        String csplace = tstartplace.getText();
        String cdplace = tdplace.getText();
        String ctprice = price.getText();
        
        int check = 0;
        
        try {
            //check for duplicate train no.
            pst = con.prepareStatement("SELECT * FROM traindetails where trainno = ?");
            pst.setString(1, ctno);//input taken
            //the variables are checked against to find the relevant train
            rs = pst.executeQuery();  
            if (rs.next () == true)
            {
                check = 1;
            }
            }
        catch (SQLException ex) {
            Logger.getLogger(registration.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (txtno.getText().equals("")||tname.getText().equals("")||tstartplace.getText().equals("")||tdplace.getText().equals("")||price.getText().equals(""))
        {
            //jButton1.setEnabled(false);
             JOptionPane.showMessageDialog(this, "Please enter all details.");
        }
        else if (check == 1)
        {
            JOptionPane.showMessageDialog(this, "Can't add duplicate train with same Train Number.");
        }
        else
        {
            try {
            pst = con.prepareStatement("insert into traindetails (trainno, trainname, startplace, destplace, price) values (?, ?, ?, ?, ?)");
            pst.setString(1, ctno);
            pst.setString(2, ctrainname);
            pst.setString(3, csplace);
            pst.setString(4, cdplace);
            pst.setString(5, ctprice);
            
            int k = pst.executeUpdate();
            if (k == 1)
            {
                JOptionPane.showMessageDialog(this, "Train Added");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "There is an error, couldnot add the train.");
            }
            txtno.setText("");
            tname.setText("");
            tstartplace.setText("");
            tdplace.setText("");
            price.setText("");
            txtno.requestFocus();
            Train_Details();
               
        } catch (SQLException ex) {
            Logger.getLogger(addtrain.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
  
    }//GEN-LAST:event_jButton1ActionPerformed

